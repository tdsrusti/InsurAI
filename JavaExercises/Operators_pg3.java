public class Operators_pg3 {
    //Logical Operators
    public static void main(String[] args) {
        int x = 10, y = 5, z = 15;
        System.out.println((x > y) && (x < z));  // true
        System.out.println((x > y) || (x > z));  // true
        System.out.println(!(x == y));           // true
    }
    
}
